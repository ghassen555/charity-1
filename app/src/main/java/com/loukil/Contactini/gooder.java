package com.loukil.Contactini;

public class gooder {
    private String nomusr ;
    private String telusr ;
    private String adrusr ;
    private String biousr ;
    private String emailusr ;
    private String idusr ;

    public gooder() {
    }

    public gooder(String nomusr, String telusr, String adrusr, String biousr, String emailusr, String idusr) {
        this.nomusr = nomusr;
        this.telusr = telusr;
        this.adrusr = adrusr;
        this.biousr = biousr;
        this.emailusr = emailusr;
        this.idusr = idusr;
    }

    public String getNomusr() {
        return nomusr;
    }

    public void setNomusr(String nomusr) {
        this.nomusr = nomusr;
    }

    public String getTelusr() {
        return telusr;
    }

    public void setTelusr(String telusr) {
        this.telusr = telusr;
    }

    public String getAdrusr() {
        return adrusr;
    }

    public void setAdrusr(String adrusr) {
        this.adrusr = adrusr;
    }

    public String getBiousr() {
        return biousr;
    }

    public void setBiousr(String biousr) {
        this.biousr = biousr;
    }

    public String getEmailusr() {
        return emailusr;
    }

    public void setEmailusr(String emailusr) {
        this.emailusr = emailusr;
    }

    public String getIdusr() {
        return idusr;
    }

    public void setIdusr(String idusr) {
        this.idusr = idusr;
    }
}
