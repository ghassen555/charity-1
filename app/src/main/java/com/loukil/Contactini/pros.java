package com.loukil.Contactini;

public class pros {
    private String u;
    private String e;
    private String pp;
    private String t;
    private String cr;
    private String b;
    private String c;
    private String pi;
    private float a;

    public pros() {
    }

    public pros(String u, String e, String pp, String t, String cr, String b, String c, String pi, float a) {
        this.u = u;
        this.e = e;
        this.pp = pp;
        this.t = t;
        this.cr = cr;
        this.b = b;
        this.c = c;
        this.pi = pi;
        this.a = a;
    }

    public String getU() {
        return u;
    }

    public void setU(String u) {
        this.u = u;
    }

    public String getE() {
        return e;
    }

    public void setE(String e) {
        this.e = e;
    }

    public String getPp() {
        return pp;
    }

    public void setPp(String pp) {
        this.pp = pp;
    }

    public String getT() {
        return t;
    }

    public void setT(String t) {
        this.t = t;
    }

    public String getCr() {
        return cr;
    }

    public void setCr(String cr) {
        this.cr = cr;
    }

    public String getB() {
        return b;
    }

    public void setB(String b) {
        this.b = b;
    }

    public String getC() {
        return c;
    }

    public void setC(String c) {
        this.c = c;
    }

    public String getPi() {
        return pi;
    }

    public void setPi(String pi) {
        this.pi = pi;
    }

    public float getA() {
        return a;
    }

    public void setA(float a) {
        this.a = a;
    }
}